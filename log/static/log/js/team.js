/*----------------------------------------------------------------------------
Enable sorting functionality
----------------------------------------------------------------------------*/
$(function(){
  $("#athletes").tablesorter();
  $('#meets').tablesorter();
});

/*----------------------------------------------------------------------------
On hover, suggest clickability
----------------------------------------------------------------------------*/
$("tr").hover(function() {
    $(this).css({"cursor": "pointer"})
});

/*----------------------------------------------------------------------------
Redirect to athlete on click
----------------------------------------------------------------------------*/
$("tr.athletes").click(function() {
    window.location.href = "/log/athlete/"+$(this).attr('id');
})